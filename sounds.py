"""
sounds.py — 140+ різних звуків «готово». Кожен звук = коротка мелодія з тонів
(частота Гц, тривалість мс), яку overlay програє через kernel32.Beep у фон-треді.
БЕЗ файлів і БЕЗ модуля random (детермінована генерація) — тому йде онлайн і не
падає на старому exe. Вибір мелодії — за лічильником тіків (overlay).
"""

# впізнавані «фірмові» мелодії
SIGNATURE = [
    [(988, 90), (1319, 300)],                                  # монетка
    [(523, 120), (523, 120), (523, 120), (659, 400)],          # фанфари
    [(392, 90), (523, 90), (659, 90), (784, 240)],             # вгору
    [(784, 90), (659, 90), (523, 90), (392, 240)],             # вниз
    [(880, 110), (1175, 240)],                                 # дінь-дон
    [(659, 80), (784, 80), (988, 80), (1319, 260)],            # рівень пройдено
    [(523, 70), (659, 70), (784, 70), (1047, 70), (784, 200)], # арпеджіо
    [(1047, 60), (988, 60), (1047, 220)],                      # пташка
    [(440, 100), (554, 100), (659, 300)],                      # мажор
    [(330, 120), (440, 120), (554, 120), (659, 360)],          # тріумф
    [(784, 70), (784, 70), (1047, 260)],                       # готово-готово
    [(587, 90), (784, 90), (587, 90), (784, 260)],             # хвилька
    [(494, 80), (659, 80), (988, 300)],                        # злет
    [(1319, 70), (1047, 70), (784, 70), (1047, 240)],          # весело
    [(262, 140), (392, 140), (523, 360)],                      # урочисто
    [(880, 60), (988, 60), (1047, 60), (1175, 60), (1319, 240)],  # драбинка
]

_SCALE = [262, 294, 330, 349, 392, 440, 494, 523, 587, 659, 698, 784, 880, 988]


def _generate():
    mel = []
    for i in range(140):
        n = 3 + (i % 5)                       # 3..7 тонів
        base = i % len(_SCALE)
        step = 1 + (i % 4)
        up = (i % 2 == 0)
        oct_up = (i // 6) % 2 == 1
        seq = []
        for k in range(n):
            idx = (base + (k if up else -k) * step) % len(_SCALE)
            f = _SCALE[idx] * (2 if oct_up else 1)
            f = max(150, min(3200, f))
            d = 70 + ((i + k) % 3) * 40
            seq.append((int(f), d))
        mel.append(seq)
    return mel


MELODIES = SIGNATURE + _generate()   # 150+


def count() -> int:
    return len(MELODIES)


def melody(i: int) -> list:
    n = len(MELODIES)
    return MELODIES[i % n] if n else [(880, 120), (1175, 220)]
