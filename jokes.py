def _cap(s):
    return s[0].upper() + s[1:] if s else s


def _generate():
    out = []
    for v in _V:
        for w in _WHEN:
            out.append(f"Денис {v} навоз {w}.")
    for w in _WHEN:
        for v in _V:
            out.append(f"{_cap(w)}, Денис {v} навоз.")
    for v in _V:
        for p in _PLACE:
            out.append(f"Денис {v} навоз {p}.")
    for h in _HAPPEN:
        for r in _REACT:
            out.append(f"Навоз {h} — Денис {r}.")
    for who in _WHO:
        for what in _WHAT:
            out.append(f"Денис любить навоз, як {who} {what}.")
    for v in _V:
        for r in _REACT:
            out.append(f"Денис {v} навоз і {r}.")
    for p in _PLACE:
        for w in _WHEN:
            out.append(f"Денис знайшов навоз {p} {w}.")
    for n in _NOUN:
        for w in _WHEN:
            out.append(f"Навоз для Дениса — {n} {w}.")
    for r in _REACT:
        for p in _PLACE:
            out.append(f"Денис {r}, коли бачить навоз {p}.")
    for n in _NOUN:
        out.append(f"Для Дениса навоз — це {n}.")
    for a in _ANS:
        out.append(f"— Де Денис? — {a}.")
    for c in _CLAIM:
        out.append(f"Денис каже: {c}.")
    for d in _DUO:
        out.append(f"Денис і навоз — {d}.")
    for r in _RUMOR:
        out.append(f"Кажуть, Денис {r}.")
    for wo in _WITHOUT:
        out.append(f"Без навозу Денис — {wo}.")
    for o in _OTHER:
        out.append(f"Навоз обрав Дениса, а не {o}.")
    for rt in _RATE:
        out.append(f"Денис поставив навозу {rt}.")
    out.extend(_PAIRS)
    return out


def _build():
    seen = set()
    res = []
    for j in HAND + _generate():
        if j not in seen:
            seen.add(j)
            res.append(j)
    return res


JOKES = _build()   # 1000+


def count() -> int:
    return len(JOKES)


def joke_at(i: int) -> str:
    n = len(JOKES)
    return JOKES[i % n] if n else ""


def effective(cfg) -> list:
    """Робочий список для показу: усі ВБУДОВАНІ (їх видаляти не можна) + свої."""
    try:
        custom = [c for c in (cfg.get("custom_jokes", []) or []) if c]
    except Exception:
        custom = []
    return JOKES + custom
